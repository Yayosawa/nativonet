package in.notyouraveragedev.tensor_image_classification.ClasesSQLite;

public class Especie {
    private String idEspecie;
    private String nomCientEspecie;
    private String nomComunEspecie;
    private String idEstadoBio;
    private String idConservacion;
    private String distribucionEspecie;
    private String descEspecie;

    public String getIdEspecie() {
        return idEspecie;
    }

    public void setIdEspecie(String idEspecie) {
        this.idEspecie = idEspecie;
    }

    public String getNomCientEspecie() {
        return nomCientEspecie;
    }

    public void setNomCientEspecie(String nomCientEspecie) {
        this.nomCientEspecie = nomCientEspecie;
    }

    public String getNomComunEspecie() {
        return nomComunEspecie;
    }

    public void setNomComunEspecie(String nomComunEspecie) {
        this.nomComunEspecie = nomComunEspecie;
    }

    public String getIdEstadoBio() {
        return idEstadoBio;
    }

    public void setIdEstadoBio(String idEstadoBio) {
        this.idEstadoBio = idEstadoBio;
    }

    public String getIdConservacion() {
        return idConservacion;
    }

    public void setIdConservacion(String idConservacion) {
        this.idConservacion = idConservacion;
    }

    public String getDistribucionEspecie() {
        return distribucionEspecie;
    }

    public void setDistribucionEspecie(String distribucionEspecie) {
        this.distribucionEspecie = distribucionEspecie;
    }

    public String getDescEspecie() {
        return descEspecie;
    }

    public void setDescEspecie(String descEspecie) {
        this.descEspecie = descEspecie;
    }
}
